func getTestPeerCerts() []*x509.Certificate {
	var r []*x509.Certificate
	for _, s := range testCerts {
		block, _ := pem.Decode([]byte(s))
		if block == nil {
			log.Panic().Msg("failed to parse certificate PEM")
		}
		cert, err := x509.ParseCertificate(block.Bytes)
		if err != nil {
			log.Panic().Err(err).Msg("failed to parse certificate PEM")
		}
		r = append(r, cert)
	}
	return r
}

func Test_findChains(t *testing.T) {
	testPeerCerts := getTestPeerCerts()
	leafCert := testPeerCerts[0]

	pool := misc.NewCertPool(testPeerCerts, false)

	chains := findChains(leafCert, []x509.Certificate{*leafCert}, pool, func(certificate *x509.Certificate) {})
	if len(chains) != 2 {
		t.Error("Could not find the two cert chains")
	}
}

func Test_findChains2(t *testing.T) {
	testPeerCerts := getTestPeerCerts()
	leafCert := testPeerCerts[0]

	pool := misc.NewCertPool(testPeerCerts, true)

	chains := findChains(leafCert, []x509.Certificate{*leafCert}, pool, func(certificate *x509.Certificate) {})
	if len(chains) < 2 {
		t.Error("Could not find the cert chains")
	}
}
