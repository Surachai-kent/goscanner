func getLECert() *x509.Certificate {
	block, _ := pem.Decode([]byte(leCert))
	if block == nil {
		log.Panic().Msg("failed to parse certificate PEM")
	}
	cert, err := x509.ParseCertificate(block.Bytes)
	if err != nil {
		log.Panic().Err(err).Msg("failed to parse certificate PEM")
	}
	return cert
}

func TestFindPotentialParentsSystemStore(t *testing.T) {

	leCert := getLECert()
	cert := findPotentialParentsSystemStore(leCert)

	if cert == nil {
		t.Fatalf("1: Cert was nil")
	}

	leCert.AuthorityKeyId = nil
	cert = findPotentialParentsSystemStore(leCert)

	if cert == nil {
		t.Fatalf("2: Cert was nil")
	}

	cert = findPotentialParentsSystemStore(&x509.Certificate{})

	if cert != nil {
		t.Fatalf("3: Cert was not nil")
	}

	cert = findPotentialParentsSystemStore(&x509.Certificate{RawIssuer: []byte{1, 2, 3}})

	if cert != nil {
		t.Fatalf("3: Cert was not nil")
	}

	cert = findPotentialParentsSystemStore(&x509.Certificate{AuthorityKeyId: []byte{2, 3, 4}})

	if cert != nil {
		t.Fatalf("3: Cert was not nil")
	}
}
